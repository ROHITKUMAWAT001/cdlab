package lab6;

public class CommentExample {

    public static void main(String[] args) {

        // This is a single-line comment.
        // It's used to explain a small piece of code.

        int number = 10; // This line declares an integer variable named 'number' and initializes it to 10.

        /*
         * This is a multi-line comment.
         * It can span across multiple lines.
         * It's useful for explaining larger blocks of code
         * or providing more detailed explanations.
         */

        System.out.println("The number is: " + number); // This line prints the value of the 'number' variable to the console.
    }
}